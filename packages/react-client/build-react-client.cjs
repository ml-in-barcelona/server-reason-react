/*
 * Generates react-client.js: React's Flight client (react-client/flight) bundled into
 * a single self-contained CJS file from the vendored React submodule.
 *
 * React doesn't publish react-client, so we build it from source: run-react-build.cjs
 * runs React's own rollup build (pointing the `react-client/flight` entry at our
 * flight-entry.js, which re-exports both the response and reply clients), then esbuild
 * bundles the output into one file (`react` left external; both dev/prod branches kept).
 *
 * Run via `npm run react-client-generate`. The output (react-client.js) is COMMITTED;
 * regenerate it only when bumping the React submodule. Consumers (react-server-dom-esbuild)
 * use the committed file, so building server-reason-react needs neither Node nor the
 * submodule — only regeneration does.
 */
const path = require("path");
const fs = require("fs");
const os = require("os");
const { execFileSync } = require("child_process");

const dir = __dirname;
const reactDir = path.join(dir, "react");
const esbuild = require(path.join(dir, "node_modules", "esbuild"));
const out = path.join(dir, "react-client.js");

/* React's build runs `npm pack` (and we may run `yarn`), which write to ~/.npm and
   ~/.cache. Point HOME at a fresh temp dir so it also works inside restricted sandboxes. */
const env = { ...process.env, HOME: fs.mkdtempSync(path.join(os.tmpdir(), "srr-react-build-")) };

if (!fs.existsSync(path.join(reactDir, "package.json"))) {
  console.error(`react submodule not checked out at ${reactDir}
       run: make submodules`);
  process.exit(1);
}

/* Install React's dev toolchain (rollup, babel, ...) once. */
if (!fs.existsSync(path.join(reactDir, "node_modules", ".bin"))) {
  execFileSync("yarn", ["install", "--frozen-lockfile"], { cwd: reactDir, stdio: "inherit", env });
}

/* Run React's rollup build in a child process and wait for it (its exit is the
   "build done" signal; execFileSync throws if it failed). */
execFileSync(process.execPath, [path.join(dir, "run-react-build.cjs")], { stdio: "inherit", env });

/* Bundle the built react-client into a single committed file, with a banner. */
esbuild.buildSync({
  entryPoints: [path.join(reactDir, "build/node_modules/react-client/flight.js")],
  outfile: out,
  bundle: true,
  format: "cjs",
  platform: "browser",
  external: ["react"],
  banner: {
    js: "// Do not edit by hand. File generated by `npm run react-client-generate`.",
  },
});
